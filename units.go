package cerebru

const (
	// unitByte represents 1 byte.
	UnitByte = 1

	// unitKB represents 1 kilobyte (1024 bytes).
	UnitKB = 1 << 10

	// unitMB represents 1 megabyte (1024 kilobytes).
	UnitMB = 1 << 20

	// unitGB represents 1 gigabyte (1024 megabytes).
	UnitGB = 1 << 30
)
